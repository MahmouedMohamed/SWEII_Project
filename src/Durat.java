package date_base;

public class Durat {
    public int Sec;
    public int Min;
    public int H;
    public void SetSec (int sec)
    {
        this.Sec=sec;
    }
    public int GetSec ()
    {
        return this.Sec;
    }
    public void SetMin (int min)
    {
        this.Min=min;
    }
    public int GetMin ()
    {
        return this.Sec;
    }
    public void SetH (int h)
    {
        this.H=h;
    }
    public int GetH ()
    {
        return this.H;
    }

}
